def t(lang: str, key: str, **kwargs: object) -> str:
    lang = lang if lang in STRINGS else "en"
    s = STRINGS[lang].get(key) or STRINGS["en"].get(key) or key
    if kwargs:
        return s.format(**kwargs)
    return s


def mode_display(lang: str, mode: object) -> str:
    name = getattr(mode, "name", str(mode))
    return t(lang, f"mode_{name}")


def algo_title(lang: str, spec: object) -> str:
    tit = getattr(spec, "title", "")
    sid = getattr(spec, "id", "")
    if lang == "zh":
        return str(tit)
    return ALGO_TITLE_EN.get(str(sid), str(tit))


def subgroup_label(lang: str, zh_name: str) -> str:
    if lang == "zh":
        return zh_name
    return SUBGROUP_ZH_TO_EN.get(zh_name, zh_name)
